    private void preencherTela(TipoAssociado tipo) {
        try {
            jTextFieldIdentificador.setText(tipo.getCodigo()+ "");
            jTextFieldDescrição.setText(tipo.getDescricao());
            jTextFieldValorMensalidade.setText(tipo.getValorMensalidade()+ "");
            jButtonExcluir.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
     }
     private void limparTela() {
        jTextFieldIdentificador.setText("");
        jTextFieldDescrição.setText("");
        jTextFieldValorMensalidade.setText("");
        
        jButtonExcluir.setEnabled(false);
        
        jTextFieldDescrição.requestFocus();
        
    }
