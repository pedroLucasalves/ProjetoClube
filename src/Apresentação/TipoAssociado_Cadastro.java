    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        try {
            TipoAssociado tipoAssociado = new TipoAssociado();
            
            tipoAssociado.setDescricao(jTextFieldDescrição.getText());
            tipoAssociado.setValorMensalidade(Double.parseDouble(
                                              jTextFieldValorMensalidade.getText()));
            if(!jTextFieldIdentificador.getText().isEmpty()){
                tipoAssociado.setCodigo(Integer.parseInt(
                                        jTextFieldIdentificador.getText()));
            }
            NTipoAssociado negocio = new NTipoAssociado();
            negocio.salvar(tipoAssociado);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Operação efetuada com" + "sucesso");
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

